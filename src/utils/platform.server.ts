function kpiKnowledgeBlock(points: KpiPoint[]): string {
  const current = latestWins(points);
  const lines: string[] = [];
  for (const cat of KPI_CATEGORIES) {
    const catLines: string[] = [];
    for (const def of KPI_METRICS[cat]) {
      const series = current
        .filter((p) => p.metric === def.key)
        .sort((a, b) => (a.period < b.period ? -1 : 1));
      if (series.length === 0) {
        catLines.push(`- ${def.label}: NO DATA`);
        continue;
      }
      const latest = series[series.length - 1];
      const prev = series.length > 1 ? series[series.length - 2] : null;
      const delta = prev
        ? ` (was ${prev.value} ${prev.unit} in ${prev.period}, change ${latest.value - prev.value >= 0 ? "+" : ""}${(latest.value - prev.value).toFixed(2)})`
        : " (single datapoint — no trend yet)";
      catLines.push(`- ${def.label}: ${latest.value} ${latest.unit} in ${latest.period}${delta}`);
    }
    lines.push(`${KPI_CATEGORY_LABELS[cat]}:\n${catLines.join("\n")}`);
  }
  return lines.join("\n\n");
}

