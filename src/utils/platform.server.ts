export async function runDiligence(
  company: string,
  website: string | undefined,
  generatedBy: string,
): Promise<PlatformContentRow> {
  return audited("platform-diligence", `Diligence run — ${company}`, async () => {
    const site = website ? ` (website: ${website})` : "";
    // Stored Signal Radar rows for this company are part of the findings too —
    // exact company match first, then a name-mention fallback across all rows.
    const signalsPromise = readSignalHeadlines({ company }, 10).then(async (exact) => {
      if (exact.length > 0 || company.trim().length <= 3) return exact;
      return readSignalHeadlines({ keywords: [company.trim().toLowerCase()] }, 10);
    });
    const [research, signalHeadlines] = await Promise.all([
      groundedSearch(
        `Research the company "${company}"${site}: what the product/technology is, funding history and investors, ` +
          `target market and business model, main competitors, traction or notable customers, and founding team. ` +
          `Be factual; say clearly when something could not be found.`,
        1500,
      ),
      signalsPromise.catch(() => [] as SignalHeadline[]),
    ]);
    if (!research.text) throw new Error("Web research returned nothing for this company");
    const internal = signalHeadlines.length
      ? `\n\nINTERNAL SIGNAL RADAR (already tracked in VenturePulse):\n${headlineBlock(signalHeadlines)}`
      : "";
    const findings = `${research.text}${internal}\n\nSources:\n${numberedSources(research.sources)}`;
    // One Vertex call for score + questions (was two) — cuts diligence quota use ~33%.
    const scored = await callGeminiJSON<{
      score?: number;
      dimensions?: Array<{ name?: string; score?: number; note?: string }>;
      rationale?: string;
      questions?: MgmtQuestionsPayload["questions"];
    }>(
      [
        "You are diligence support for a deep-tech VC (security, AI, data, cloud, infrastructure, silicon, supply chain).",
        "Score ONLY from the provided findings; do not invent facts.",
        "Also draft management questions grounded in findings / weak dimensions — no generic questionnaires.",
        'Group questions by area ("Product", "Market", "Traction", "Team", or "Risks").',
        'Output ONLY JSON: {"score":<1-10>,"dimensions":[{"name":"","score":<1-10>,"note":""}],"rationale":"","questions":[{"area":"","question":"","why":""}]}',
      ].join(" "),
      `Company: ${company}\n\nFindings:\n${findings}`,
      2048,
    );
    if (!scored.ok || !scored.data) throw new Error(scored.error || "Thesis scoring failed");
    const dimensions: DiligenceDimension[] = Array.isArray(scored.data.dimensions)
      ? scored.data.dimensions
          .map((d) => ({
            name: String(d?.name ?? ""),
            score: Number(d?.score ?? 0),
            note: String(d?.note ?? ""),
          }))
          .filter((d) => d.name)
      : [];
    const payload: DiligencePayload = {
      score: Number(scored.data.score ?? 0),
      dimensions,
      rationale: String(scored.data.rationale ?? ""),
      questions: Array.isArray(scored.data.questions)
        ? scored.data.questions.filter((x) => x?.question)
        : [],
      sources: research.sources.map((s) => s.uri),
      signalsUsed: signalHeadlines.map(
        (s) => `${s.date ? `[${s.date}] ` : ""}${s.category ? `(${s.category}) ` : ""}${s.signal}`,
      ),
    };
    return savePlatformContent({
      type: "diligence",
      subject: company,
      title: `Diligence — ${company}`,
      payload,
      sources: payload.sources,
      generatedBy,
    });
  });
}

