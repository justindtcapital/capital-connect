// Grounding rules:
// - Every URL that reaches the user comes from Gemini grounding metadata;
//   model-written URLs outside that set are dropped in code.
// - KNOWLEDGE fed to Gems is limited to user-entered KPI rows, stored
//   signals, Sheet-sourced company profiles, and grounded web results.
//   Asana-sourced fields NEVER reach a prompt (confidentiality wall —
