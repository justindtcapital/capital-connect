// ── Header-name-based row access (order-independent, like mapRows) ──

function headerIndex(rows: string[][]): Record<string, number> {
  const idx: Record<string, number> = {};
  (rows[0] ?? []).forEach((h, i) => {
    idx[h.trim().toLowerCase()] = i;
  });
  return idx;
}

function cell(row: string[], i: number | undefined): string {
  return i == null ? "" : (row[i] ?? "").trim();
}

async function readTab(tabName: string): Promise<string[][]> {
  // A tab that doesn't exist yet simply reads as empty.
  return fetchSheetTab(tabName).catch(() => [] as string[][]);
}

// ── PortCo KPIs ──────────────────────────────────────────────────

export async function buildPortcoKpis(): Promise<KpiPoint[]> {
  const rows = await readTab(TAB_NAMES.portcoKpis);
  if (rows.length < 2) return [];
  const h = headerIndex(rows);
  const points: KpiPoint[] = [];
  for (const row of rows.slice(1)) {
    const value = Number(cell(row, h["value"]).replace(/[$,%\s]/g, ""));
    const metric = cell(row, h["metric"]);
    if (!metric || !Number.isFinite(value)) continue;
    points.push({
      id: cell(row, h["id"]),
      portcoUrid: cell(row, h["portco urid"]),
      companyName: cell(row, h["company name"]),
      category: cell(row, h["category"]),
      metric,
      period: cell(row, h["period"]),
      value,
      unit: cell(row, h["unit"]),
      source: cell(row, h["source"]),
      note: cell(row, h["note"]),
      enteredBy: cell(row, h["entered by"]),
      enteredAt: cell(row, h["entered at"]),
    });
  }
  return points;
}

export interface AppendKpiOptions {
  action: OpsLogAction; // "edit" for manual adds, "import" for smart-paste, "enrich" for web
  source: string; // ops-log source, e.g. "platform-kpi" / "platform-digital"
  summary: string;
}

/**
 * Append datapoints in ONE batched write. Category and unit derive from the
 * KPI_METRICS registry; rows with unknown metrics or unparseable periods are
 * rejected up front so a bad smart-paste can't pollute the tab.
 * Pass `ops` to audit the write; omit it when the caller logs its own event.
 */
export async function appendKpiPoints(
  points: NewKpiPoint[],
  enteredBy: string,
  ops?: AppendKpiOptions,
): Promise<number> {
  const now = new Date().toISOString();
  const rows: string[][] = [];
  for (const p of points) {
    const def = metricDef(p.metric);
    if (!def) throw new Error(`Unknown KPI metric "${p.metric}"`);
    const period = normalizePeriod(p.period);
    if (!period) throw new Error(`Unparseable period "${p.period}" for ${def.label}`);
    if (!Number.isFinite(p.value)) throw new Error(`Invalid value for ${def.label}`);
    rows.push([
      crypto.randomUUID(),
      (p.portcoUrid ?? "").trim(),
      p.companyName.trim(),
      metricCategory(p.metric) ?? "",
      p.metric,
      period,
      String(p.value),
      def.unit,
      p.source,
      p.note ?? "",
      enteredBy,
      now,
    ]);
  }
  if (rows.length === 0) return 0;
  await ensureTab(TAB_NAMES.portcoKpis, PORTCO_KPI_HEADERS);
  await appendSheetRows(TAB_NAMES.portcoKpis, rows);
  if (ops) {
    await logOpsEvent({
      action: ops.action,
      source: ops.source,
      status: "ok",
      summary: ops.summary,
      records: rows.length,
      items: rows.map((r) => `${r[2]} · ${r[4]} = ${r[6]} (${r[5]})`),
    });
  }
  return rows.length;
}

export async function deleteKpiPoint(id: string): Promise<boolean> {
  const rows = await readTab(TAB_NAMES.portcoKpis);
  if (rows.length < 2) return false;
  const h = headerIndex(rows);
  const idx = rows.slice(1).findIndex((row) => cell(row, h["id"]) === id);
  if (idx === -1) return false;
  const target = rows[idx + 1];
  await deleteSheetRows(TAB_NAMES.portcoKpis, [idx + 2]);
  await logOpsEvent({
    action: "delete",
    source: "platform-kpi",
    status: "ok",
    summary: `Deleted KPI datapoint ${cell(target, h["metric"])} for ${cell(target, h["company name"])}`,
    records: 1,
  });
  return true;
}

// ── Platform Content (saved AI generations) ──────────────────────

const SOURCES_SEP = "\n";

export async function buildPlatformContent(
  type?: PlatformContentType,
): Promise<PlatformContentRow[]> {
  const rows = await readTab(TAB_NAMES.platformContent);
  if (rows.length < 2) return [];
  const h = headerIndex(rows);
  const out: PlatformContentRow[] = [];
  for (const row of rows.slice(1)) {
    const rowType = cell(row, h["type"]);
    if (!isContentType(rowType)) continue;
    if (type && rowType !== type) continue;
    let payload: PlatformContentPayload;
    try {
      // Reassemble the JSON from the primary cell + any overflow cells. Use raw
      // (untrimmed) access — cell() trims, which would corrupt a chunk boundary
      // that lands on whitespace inside a string value. Older rows have only the
      // primary "JSON" column; the overflow columns read as "".
      const rawCell = (name: string) => {
        const i = h[name];
        return i == null ? "" : (row[i] ?? "");
      };
      const json = rawCell("json") + JSON_OVERFLOW_COLS.map(rawCell).join("");
      payload = JSON.parse(json) as PlatformContentPayload;
    } catch {
      continue; // unparseable payloads are dropped, never fatal
    }
    out.push({
      id: cell(row, h["id"]),
      type: rowType,
      subject: cell(row, h["subject"]),
      portcoUrid: cell(row, h["portco urid"]),
      title: cell(row, h["title"]),
      payload,
      sources: cell(row, h["sources"]).split(SOURCES_SEP).map((s) => s.trim()).filter(Boolean),
      generatedAt: cell(row, h["generated at"]),
      generatedBy: cell(row, h["generated by"]),
    });
  }
  // Newest first — ISO timestamps sort lexicographically.
  return out.sort((a, b) => (a.generatedAt < b.generatedAt ? 1 : -1));
}

export interface SaveContentInput {
  type: PlatformContentType;
  subject: string;
  portcoUrid?: string;
  title: string;
  payload: PlatformContentPayload;
  sources: string[];
  generatedBy: string;
}

export async function savePlatformContent(input: SaveContentInput): Promise<PlatformContentRow> {
  const row: PlatformContentRow = {
    id: crypto.randomUUID(),
    type: input.type,
    subject: input.subject.trim(),
    portcoUrid: (input.portcoUrid ?? "").trim(),
    title: input.title.trim(),
    payload: input.payload,
    sources: input.sources,
    generatedAt: new Date().toISOString(),
    generatedBy: input.generatedBy,
  };
  await ensureTab(TAB_NAMES.platformContent, PLATFORM_CONTENT_HEADERS);
  // Existing tabs were created with the old 9-column header; extend it so the
  // overflow columns are addressable by name on read-back.
  await ensureHeaderWidth(TAB_NAMES.platformContent, PLATFORM_CONTENT_HEADERS);
  // Split the payload JSON across the primary + overflow cells to stay under the
  // 50k-char-per-cell limit.
  const chunks = chunkString(JSON.stringify(row.payload), MAX_CELL_CHARS);
  if (chunks.length > 1 + JSON_OVERFLOW_COLS.length) {
    throw new Error("Generated content is too large to store — try a narrower theme.");
  }
  const overflow = JSON_OVERFLOW_COLS.map((_, i) => chunks[i + 1] ?? "");
  await appendSheetRow(TAB_NAMES.platformContent, [
    row.id,
    row.type,
    row.subject,
    row.portcoUrid,
    row.title,
    chunks[0] ?? "",
    row.sources.join(SOURCES_SEP),
    row.generatedAt,
    row.generatedBy,
    ...overflow,
  ]);
  return row;
}

