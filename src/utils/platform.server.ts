// Locate a row by ID and update named columns in one batched cell write.
// Returns false when the ID isn't present (tab missing or row deleted).
async function updateRowCells(
  tabName: string,
  id: string,
  values: Record<string, string>,
): Promise<boolean> {
  const rows = await readTab(tabName);
  if (rows.length < 2) return false;
  const h = headerIndex(rows);
  const idx = rows.slice(1).findIndex((row) => cell(row, h["id"]) === id);
  if (idx === -1) return false;
  const rowNumber = idx + 2; // 1-based, after the header row
  const updates: { range: string; value: string }[] = [];
  for (const [header, value] of Object.entries(values)) {
    const col = h[header];
    if (col == null) continue;
    updates.push({ range: `${colLetters(col)}${rowNumber}`, value });
  }
  await updateSheetCells(tabName, updates);
  return true;
}

