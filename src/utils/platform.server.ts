interface SignalHeadline {
  date: string;
  company: string;
  category: string;
  signal: string;
}

// Light read of the stored Signals tab (header-aware) for grounding blocks.
async function readSignalHeadlines(
  filter: { company?: string; keywords?: string[] },
  limit: number,
): Promise<SignalHeadline[]> {
  const rows = await readTab(TAB_NAMES.signals);
  if (rows.length < 2) return [];
  const h = headerIndex(rows);
  const company = (filter.company ?? "").trim().toLowerCase();
  const keywords = (filter.keywords ?? []).map((k) => k.toLowerCase()).filter((k) => k.length > 3);
  const out: SignalHeadline[] = [];
  for (const row of rows.slice(1)) {
    const rowCompany = cell(row, h["company"]);
    const signal = cell(row, h["signal"]);
    if (!signal) continue;
    if (company && rowCompany.toLowerCase() !== company) continue;
    if (!company && keywords.length) {
      const hay = `${rowCompany} ${signal} ${cell(row, h["category"])}`.toLowerCase();
      if (!keywords.some((k) => hay.includes(k))) continue;
    }
    out.push({
      date: cell(row, h["date found"]),
      company: rowCompany,
      category: cell(row, h["category"]),
      signal,
    });
  }
  return out.sort((a, b) => (a.date < b.date ? 1 : -1)).slice(0, limit);
}

function headlineBlock(headlines: SignalHeadline[]): string {
  return headlines
    .map((s) => `- ${s.date ? `[${s.date}] ` : ""}${s.company}${s.category ? ` (${s.category})` : ""}: ${s.signal}`)
    .join("\n");
}

function themeKeywords(theme: string): string[] {
  return theme.toLowerCase().split(/[^a-z0-9+]+/).filter((w) => w.length > 3);
}

