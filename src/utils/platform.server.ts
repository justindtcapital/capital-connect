// Candidate companies from the stored Signals tab: hook-matched rows first,
// then the most recent others (the Gem drops non-fits, so over-supplying is
// fine — the cap just bounds tokens). Portfolio companies never qualify.
interface SignalCandidate {
  company: string;
  lines: string[];
  urls: string[];
}

async function readSignalCandidates(
  hooks: string[],
  portcoNames: Set<string>,
  maxCompanies = 20,
): Promise<SignalCandidate[]> {
  const rows = await readTab(TAB_NAMES.signals);
  if (rows.length < 2) return [];
  const h = headerIndex(rows);
  const lowerHooks = hooks.map((k) => k.toLowerCase()).filter((k) => k.length > 2);
  interface Entry {
    candidate: SignalCandidate;
    hooked: boolean;
    latest: string;
  }
  const byCompany = new Map<string, Entry>();
  for (const row of rows.slice(1)) {
    const company = cell(row, h["company"]);
    const signal = cell(row, h["signal"]);
    if (!company || !signal) continue;
    const key = company.toLowerCase();
    if (portcoNames.has(key)) continue;
    const date = cell(row, h["date found"]);
    const url = cell(row, h["source url"]);
    const hay = `${company} ${signal} ${cell(row, h["category"])}`.toLowerCase();
    const hooked = lowerHooks.some((k) => hay.includes(k));
    let entry = byCompany.get(key);
    if (!entry) {
      entry = { candidate: { company, lines: [], urls: [] }, hooked: false, latest: "" };
      byCompany.set(key, entry);
    }
    if (entry.candidate.lines.length < 3) {
      entry.candidate.lines.push(`${date ? `[${date}] ` : ""}${signal}`);
      if (/^https?:\/\//i.test(url)) entry.candidate.urls.push(url);
    }
    entry.hooked = entry.hooked || hooked;
    if (date > entry.latest) entry.latest = date;
  }
  return [...byCompany.values()]
    .sort((a, b) => Number(b.hooked) - Number(a.hooked) || (a.latest < b.latest ? 1 : -1))
    .slice(0, maxCompanies)
    .map((e) => e.candidate);
}

