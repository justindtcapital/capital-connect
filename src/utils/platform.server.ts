// ══════════════════════════════════════════════════════════════

interface GroundedSource {
  /** Resolved publisher URL when we could follow the redirect; else the raw grounding URL. */
  uri: string;
  title: string;
  /** Human-readable host for display (e.g. "goldmansachs.com"). */
  domain?: string;
}

interface GroundedSearchResult {
  text: string;
  sources: GroundedSource[];
}

/** Bare hostname (no www.) for citation display; "" if the URL won't parse. */
function hostOf(url: string): string {
  try {
    return new URL(url).hostname.replace(/^www\./i, "");
  } catch {
    return "";
  }
}

// Gemini grounding hands back ephemeral vertexaisearch "grounding-api-redirect"
// URLs — unreadable and short-lived. Follow the redirect once (bounded, best
// effort) to capture the durable publisher URL. Falls back to the redirect URL
// on any timeout/error so generation never blocks on citation resolution.
async function resolveGroundingUrl(uri: string): Promise<string> {
  if (!/grounding-api-redirect|vertexaisearch/i.test(uri)) return uri;
  const ctrl = new AbortController();
  const timer = setTimeout(() => ctrl.abort(), 4500);
  try {
    const res = await fetch(uri, { method: "GET", redirect: "follow", signal: ctrl.signal });
    const finalUrl = res.url || uri;
    // We only want the resolved URL, not the page body.
    try {
      await res.body?.cancel();
    } catch {
      /* ignore */
    }
    return /grounding-api-redirect|vertexaisearch/i.test(finalUrl) ? uri : finalUrl;
  } catch {
    return uri;
  } finally {
    clearTimeout(timer);
  }
}

// Resolve a batch of grounding URLs to real publisher URLs and stamp a display
// domain on each, with bounded parallelism.
async function resolveSources(sources: GroundedSource[]): Promise<GroundedSource[]> {
  const CONCURRENCY = 12;
  for (let i = 0; i < sources.length; i += CONCURRENCY) {
    const slice = sources.slice(i, i + CONCURRENCY);
    const resolved = await Promise.all(slice.map((s) => resolveGroundingUrl(s.uri)));
    resolved.forEach((uri, j) => {
      slice[j].uri = uri;
      // When resolution failed the URL is still a Google redirect — its host is
      // useless for display, so fall back to the publisher title Gemini gave us.
      const stillRedirect = /grounding-api-redirect|vertexaisearch/i.test(uri);
      slice[j].domain = stillRedirect ? slice[j].title : hostOf(uri) || slice[j].title;
    });
  }
  return sources;
}

// Isolated google_search grounding call (the built-in tool can't be combined
// with JSON mode or custom functions, so the answer arrives as plain text).
async function groundedSearch(prompt: string, answerTokens = 1200): Promise<GroundedSearchResult> {
  let resp: GeminiResponse;
  try {
    resp = await geminiGenerate({
      contents: [{ role: "user", parts: [{ text: prompt }] }],
      tools: [{ googleSearch: {} }],
      generationConfig: {
        maxOutputTokens: answerTokens + 2048,
        thinkingConfig: { thinkingBudget: 2048 },
      },
    });
  } catch (e) {
    throw new Error(e instanceof Error ? e.message : "Web search failed");
  }
  const cand = resp.candidates?.[0];
  const text = (cand?.content?.parts || [])
    .filter((p) => typeof p.text === "string")
    .map((p) => p.text as string)
    .join("")
    .trim();
  const gm = cand?.groundingMetadata as
    | { groundingChunks?: Array<{ web?: { uri?: string; title?: string } }> }
    | undefined;
  const seen = new Set<string>();
  const sources: GroundedSource[] = [];
  for (const ch of gm?.groundingChunks || []) {
    const uri = ch?.web?.uri?.trim();
    if (uri && !seen.has(uri)) {
      seen.add(uri);
      sources.push({ uri, title: (ch?.web?.title || "").trim() });
    }
  }
  // Turn ephemeral redirect URLs into durable, readable publisher URLs.
  await resolveSources(sources);
  return { text, sources };
}

// Grounded calls return prose, so JSON inside must be dug out locally
// (gemini.server's extractor is private and tied to JSON-mode calls).
function extractJsonLoose(text: string): unknown | null {
  const cleaned = text.replace(/```(?:json)?/gi, "");
  const start = cleaned.indexOf("{");
  const end = cleaned.lastIndexOf("}");
  if (start === -1 || end <= start) return null;
  try {
    return JSON.parse(cleaned.slice(start, end + 1));
  } catch {
    return null;
  }
}

function numberedSources(sources: GroundedSource[]): string {
  return sources
    .map((s, i) => {
      const label = [s.title, s.domain].filter(Boolean).join(" · ");
      return `[${i + 1}] ${label ? `${label} — ` : ""}${s.uri}`;
    })
    .join("\n");
}

// Ops-log wrapper for the generate flows: failures are audited too, then
// rethrown so the UI can toast them.
async function audited<T>(source: string, summary: string, fn: () => Promise<T>): Promise<T> {
  try {
    const result = await fn();
    await logOpsEvent({ action: "enrich", source, status: "ok", summary, records: 1 });
    return result;
  } catch (e) {
    await logOpsEvent({
      action: "enrich",
      source,
      status: "error",
      summary: `${summary} — ${e instanceof Error ? e.message : "failed"}`,
    });
    throw e;
  }
}

