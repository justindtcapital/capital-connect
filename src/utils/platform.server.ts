export async function fetchDigitalTraction(
  portco: { urid?: string; name: string; website?: string },
  enteredBy: string,
): Promise<DigitalTractionResult> {
  return audited("platform-digital", `Web refresh: digital traction for ${portco.name}`, async () => {
    const site = portco.website ? ` (website: ${portco.website})` : "";
    const { text, sources } = await groundedSearch(
      `Search the web for current public digital-presence stats of the company "${portco.name}"${site}. ` +
        `Find: LinkedIn follower count, X (Twitter) follower count, number of press/news mentions in the last 90 days, ` +
        `and global web-traffic rank. End your answer with ONLY this JSON (null for anything you could not find; ` +
        `never guess): {"linkedin_followers":null,"x_followers":null,"press_mentions_90d":null,"traffic_rank_global":null}`,
      900,
    );
    // No grounding chunks = nothing verifiable — store nothing.
    if (sources.length === 0) {
      return { added: 0, points: [], evidence: [], note: "No grounded web results found." };
    }
    const parsed = (extractJsonLoose(text) ?? {}) as Record<string, unknown>;
    const evidenceNote = `web · ${sources.slice(0, 2).map((s) => s.uri).join(" ")}`;
    const period = currentPeriod();
    const points: NewKpiPoint[] = [];
    for (const key of DIGITAL_KEYS) {
      const value = Number(parsed[key]);
      if (parsed[key] == null || !Number.isFinite(value) || value < 0) continue;
      points.push({
        portcoUrid: portco.urid ?? "",
        companyName: portco.name,
        metric: key,
        period,
        value,
        source: "gemini_web",
        note: evidenceNote,
      });
    }
    // No ops arg: the surrounding audited() wrapper writes the single
    // enrich log entry for this refresh.
    if (points.length > 0) await appendKpiPoints(points, enteredBy);
    return {
      added: points.length,
      points,
      evidence: sources.slice(0, 5),
      note:
        points.length === 0
          ? "Search ran but no verifiable numbers were found."
          : `Best-effort public data — ${points.length} of ${DIGITAL_KEYS.length} metrics found.`,
    };
  });
}

